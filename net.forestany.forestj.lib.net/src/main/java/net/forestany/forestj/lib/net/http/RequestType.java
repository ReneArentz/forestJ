package net.forestany.forestj.lib.net.http;

/**
 * All supported http(s) request types.
 * 
 * GET
 * POST
 * DOWNLOAD
 */
public enum RequestType {
	/**
	 * GET request type
	 */
	GET,
	/**
	 * POST request type
	 */
	POST,
	/**
	 * PUT request type
	 */
	PUT,
	/**
	 * DELETE request type
	 */
	DELETE,
	/**
	 * DOWNLOAD request type
	 */
	DOWNLOAD
}
