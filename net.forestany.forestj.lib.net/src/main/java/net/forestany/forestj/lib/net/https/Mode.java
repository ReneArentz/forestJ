package net.forestany.forestj.lib.net.https;

/**
 * All supported modes for tiny https/soap/rest server.
 * 
 * NORMAL
 * DYNAMIC
 * SOAP
 * REST
 */
public enum Mode {
	/**
	 * NORMAL mode
	 */
	NORMAL,
	/**
	 * DYNAMIC mode
	 */
	DYNAMIC,
	/**
	 * SOAP mode
	 */
	SOAP,
	/**
	 * REST mode
	 */
	REST
}
