package net.forestany.forestj.lib.sqlcore;

/**
 * Enumeration for all supported sql types.
 */
public enum SqlType {
	/**
	 * SELECT sql type
	 */
	SELECT,
	/**
	 * INSERT sql type
	 */
	INSERT,
	/**
	 * UPDATE sql type
	 */
	UPDATE,
	/**
	 * DELETE sql type
	 */
	DELETE,
	/**
	 * TRUNCATE sql type
	 */
	TRUNCATE,
	/**
	 * CREATE sql type
	 */
	CREATE,
	/**
	 * DROP sql type
	 */
	DROP,
	/**
	 * ALTER sql type
	 */
	ALTER
}
